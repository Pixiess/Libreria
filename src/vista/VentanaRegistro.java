    public Libro getLibroAEditar(){
        Libro respuesta = null;
        
        if(libroElegido)
            respuesta = registroLibros.get(filaSeleccionada);
        return respuesta;
    }
    
    public void establecerDatosTabla(){
        registroLibros = controladorLibro.getReportePorFiltro("Por Titulo");
        filaSeleccionada = 0;
        Object[][] arregloDatosLibro = conseguirDatosLibro(registroLibros);
        tableModel.setDataVector(arregloDatosLibro, titulosTabla);        
    }
    
    private Object[][] conseguirDatosLibro(ArrayList<Libro> registroLibros){
        Object[][] respuesta = new Object[registroLibros.size()][];
        
        for (int i = 0; i < respuesta.length; i++) {
            respuesta[i] = convertirLibroAArreglo(registroLibros.get(i));
        }
        
        return respuesta;
    }
    
    private Object[] convertirLibroAArreglo(Libro libro){
        Object [] respuesta = new Object[6];
        
        respuesta[0] = libro.getIdLibro();
        respuesta[1] = libro.getNombreLibro();
        respuesta[2] = libro.getAutorLibro();
        respuesta[3] = libro.getEdicion();
        respuesta[4] = libro.getStockDisponible();
        respuesta[5] = libro.getStockMinimo();
        
        return respuesta;
    }

