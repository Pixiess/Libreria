    private void btnEditarLibroMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarLibroMouseClicked
        int countRow = getVentanaLibros().getRowCount();
        if(countRow > 0)
        {
            int row = getVentanaLibros().getSelectedRow();
            if(row >= 0)
            {
                libroElegido = true;
                editarLibro(row);
            }
            else
            {
                 JOptionPane.showMessageDialog(null, "Seleccione una fila para editar el libro");
            }
        }
    }//GEN-LAST:event_btnEditarLibroMouseClicked

